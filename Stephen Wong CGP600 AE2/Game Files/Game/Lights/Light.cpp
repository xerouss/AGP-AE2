#pragma region Includes

#include "Light.h"

#pragma endregion

//####################################################################################
// Constructor
//####################################################################################
Light::Light(float red, float green, float blue)
{
	SetLightColour(red, green, blue);
}

//####################################################################################
// Get light colour
//####################################################################################
XMVECTOR Light::GetLightColour(void)
{
	return m_colour;
}

//####################################################################################
// Set light colour
//####################################################################################
void Light::SetLightColour(float red, float green, float blue)
{
	m_colour = XMVectorSet(red, green, blue, 1);
}
